var currentLang = localStorage.getItem('casadecorona-lang') || 'en';

function setLang(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  localStorage.setItem('casadecorona-lang', lang);
  document.documentElement.lang = lang;
  applyLanguage();
  document.querySelectorAll('.lang-switch [data-lang]').forEach(function (btn) {
    btn.setAttribute('aria-pressed', btn.getAttribute('data-lang') === lang ? 'true' : 'false');
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

function applyLanguage() {
  var tr = I18N[currentLang];
  if (!tr) return;
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.getAttribute('data-i18n');
    if (tr[key] != null) el.textContent = tr[key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (tr[key] != null) el.placeholder = tr[key];
  });
  document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-html');
    if (tr[key] != null) el.innerHTML = tr[key];
  });
  if (typeof updatePriceLabels === 'function') updatePriceLabels();
}

document.addEventListener('DOMContentLoaded', function () {
  document.documentElement.lang = currentLang;
  applyLanguage();
  document.querySelectorAll('.lang-switch [data-lang]').forEach(function (btn) {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === currentLang);
    btn.setAttribute('aria-pressed', btn.getAttribute('data-lang') === currentLang ? 'true' : 'false');
    btn.addEventListener('click', function () {
      setLang(btn.getAttribute('data-lang'));
    });
  });
});
